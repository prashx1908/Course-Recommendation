# Load the new course list from the generator script
from custom_specialization_matrix_generator import courses

# Read the old matrix
old_matrix_path = "corrected_specialization_matrix (1).csv"
df = pd.read_csv(old_matrix_path)

# The first column is the row label, the rest are columns
old_rows = df.iloc[:, 0].tolist()
old_cols = df.columns.tolist()[1:]

# Build a set for fast lookup
course_set = set(courses)

# Build relevancy dictionary for exact matches only
relevancy = {}

for i, row_name in enumerate(old_rows):
    if row_name not in course_set:
        continue
    for j, col_name in enumerate(old_cols):
        if col_name not in course_set:
            continue
        value = df.iloc[i, j+1]  # +1 because first col is row label
        try:
            value = int(value)
        except:
            continue
        relevancy[(row_name, col_name)] = value

# Output as a Python file for import
