    f.write("# Auto-generated relevancy dictionary from old matrix (exact matches only)\n")
    f.write("relevancy = {\n")
    for (row, col), val in relevancy.items():
        f.write(f"    ({row!r}, {col!r}): {val},\n")
    f.write("}\n")

